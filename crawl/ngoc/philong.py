class Item:

  def set_title(self, title):
    self.title = title;

  def get_title(self):
    return self.title

  def set_warranty(self, warranty):
    self.warranty = warranty
  
  def get_warranty(self):
    return self.warranty

  def set_price(self, price):
    self.price = price

  def get_price(self):
    return self.price

  def set_item_image_origin(self, item_image_origin):
    self.item_image_origin = item_image_origin

  def get_item_image_origin(self):
    return self.item_image_origin

  def set_item_image_resize(self, item_image_resize):
    self.item_image_resize = item_image_resize

  def get_item_image_resize(self):
    return self.item_image_resize

  def set_item_infos(self, item_infos):
    self.item_infos = item_infos

  def get_item_infos(self):
    return self.item_infos

  def set_item_infos_technical(self, item_infos_technical):
    self.item_infos_technical = item_infos_technical

  def get_item_infos_technical(self):
    return self.item_infos_technical

  